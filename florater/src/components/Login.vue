<template>
  <form action="#" @submit.prevent="log">
    <v-text-field
      label="E-mail"
      name="email"
      v-model="email"
      :error-messages="emailErrors"
      :counter="30"
      autocomplete="on"
      required
      @input="$v.email.$touch()"
      @blur="$v.email.$touch()"
    ></v-text-field>
    <v-text-field
      label="Hasło"
      name="password"
      v-model="password"
      type="password"
      :error-messages="passwordErrors"
      required
      @input="$v.password.$touch()"
      @blur="$v.password.$touch()"
    ></v-text-field>
    <v-btn round color="primary" @click="log">Zaloguj się</v-btn>
    <v-btn round color="grey" class="white--text" @click="clear">Wyczyść</v-btn>
  </form>
</template>

<script>
import { required, maxLength, minLength } from "vuelidate/lib/validators";
import { validationMixin } from "vuelidate";

export default {
  mixins: [validationMixin],
  validations: {
    email: { required, minLength: minLength(1), maxLength: maxLength(30) },
    password: { required, minLength: minLength(1) }
  },
  data() {
    return {
      email: this.email,
      password: this.password
    };
  },

  computed: {
    emailErrors() {
      const errors = [];
      if (!this.$v.email.$dirty) return errors;
      !this.$v.email.minLength &&
        errors.push("E-mail musi posiadać co najmniej 1 znak");
      !this.$v.email.maxLength &&
        errors.push("E-mail nie może być większy niż 30 znaków");
      !this.$v.email.required && errors.push("E-mail jest wymagany");
      return errors;
    },
    passwordErrors() {
      const errors = [];
      if (!this.$v.password.$dirty) return errors;
      !this.$v.password.minLength &&
        errors.push("Hasło musi posiadać co najmniej 1 znak");
      !this.$v.password.required && errors.push("Hasło jest wymagane");
      return errors;
    }
  },

  methods: {
    clear() {
      this.$v.$reset();
      this.email = "";
      this.password = "";
    }
  },

  log() {
    const axios = require("axios");
    let JWTtoken = null;

    const Login = () => {
      axios
        .post("http://127.0.0.1:8000/users/invalidate-token/", {
          headers: {
            Authorization: `JWT ${JWTtoken}`
          },
          email: this.email,
          password: this.password
        })
        .then(response => {
          console.log(response);
          const JWTtoken = response.data.access_token;

          localStorage.setItem("access_token", JWTtoken);
          context.commit("retrieveToken", JWTtoken);
          resolve(response);
        })
        .catch(error => {
          console.log(error);
          reject(error);
        });
    };
  }
};
</script>


